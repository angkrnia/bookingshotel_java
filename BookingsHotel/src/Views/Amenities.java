        String[] id = amenityCombo.getSelectedItem().toString().split("-");
        String query = "INSERT INTO roomamenity VALUES(null,?,?)";
        try {
            ps = conn.prepareStatement(query);
            ps.setString(1, roomId);
            ps.setString(2, id[0]);
            if(ps.executeUpdate() > 0){
                JOptionPane.showMessageDialog(null, "Room Amenity has successfull added!");
                getAmenitiesListCombo();
                getAmenitiesRoom();
            }
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"Insert failed",JOptionPane.ERROR_MESSAGE);
        }
