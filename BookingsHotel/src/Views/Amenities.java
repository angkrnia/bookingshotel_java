    public void getAmenitiesListCombo(){
        try {
            String query = "SELECT * from amenities a where a.id not in "
                    + "(SELECT amenityId from roomamenity left join rooms on rooms.id=roomamenity.roomId "
                    + "WHERE roomNumber = ?)";
            ps = conn.prepareStatement(query);
            ps.setString(1, room);
            rs = ps.executeQuery();
            tb_model.setNumRows(0);
            int rows = 0;
            amenityCombo.removeAllItems();
            while(rs.next()){
                amenityCombo.addItem(rs.getString("id")+"-"+rs.getString("amenityName"));
                rows++;
            }
            if(rows==0){
                amenityCombo.addItem("");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void getAmenitiesRoom(){
        try {
            String query = "SELECT amenities.amenityName as name FROM roomamenity "
                    + "INNER JOIN amenities ON amenities.id=roomamenity.amenityId "
                    + "INNER JOIN rooms on rooms.id=roomamenity.roomId where rooms.roomNumber=?";
            ps = conn.prepareStatement(query);
            ps.setString(1, room);
            rs = ps.executeQuery();
            tb_model.setNumRows(0);
            int no = 1;
            while(rs.next()){
                Object data[] = {
                    no,
                    rs.getString("name"),
                    "Available",
                    "Standart",
                };
                tb_model.addRow(data);
                no++;
            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

