    private void roomstableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_roomstableMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = roomstable.getSelectedRow();
        if(selectedRowIndex >= 0){
            update.setVisible(true);
            delete.setVisible(true);
            clear.setVisible(true);
            userid.setText(tb_model.getValueAt(selectedRowIndex, 0).toString());
            username.setText(tb_model.getValueAt(selectedRowIndex, 1).toString());
            fullname.setText(tb_model.getValueAt(selectedRowIndex, 2).toString());
            password.setText(tb_model.getValueAt(selectedRowIndex, 3).toString());
            //level.setText(tb_model.getValueAt(selectedRowIndex, 4).toString());
            if(null != tb_model.getValueAt(selectedRowIndex, 4).toString())switch (tb_model.getValueAt(selectedRowIndex, 4).toString()) {
                case "Staf":
                    level.setSelectedIndex(0);
                    break;
                case "Guest":
                    level.setSelectedIndex(1);
                    break;
                default:
                break;
            }
        }
    }//GEN-LAST:event_roomstableMouseClicked

