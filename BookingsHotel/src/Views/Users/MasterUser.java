    public void getUsers(){
        tb_model.setNumRows(0);
        try {
            Statement stm = conn.createStatement();
            rs = stm.executeQuery("SELECT * FROM users ORDER BY id ASC");
            while(rs.next()){
                Object data[] = {
                    rs.getInt("id"),
                    rs.getString("username"),
                    rs.getString("fullname"),
                    rs.getString("password"),
                    rs.getString("level")
                };
                tb_model.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        } 
    }
    
    public void getDetails(String id){
        try {
            Statement stm = conn.createStatement();
            rs = stm.executeQuery("SELECT * FROM users where id="+id);
            while(rs.next()){
                update.setVisible(true);
                delete.setVisible(true);
                clear.setVisible(true);
                userid.setText(id);
                username.setText(rs.getString("username"));
                password.setText(rs.getString("password"));
                fullname.setText(rs.getString("fullname"));
                if(null != rs.getString("level"))switch (rs.getString("level")) {
                case "Staf":
                    level.setSelectedIndex(0);
                    break;
                case "Guest":
                    level.setSelectedIndex(1);
                    break;
                default:
                break;
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        } 
    }
    
    public void clear(){
        update.setVisible(false);
        delete.setVisible(false);
        clear.setVisible(false);
        userid.setText(null);
        username.setText(null);
        password.setText(null);
        fullname.setText(null);
        level.setSelectedIndex(0);
        roomstable.getSelectionModel().clearSelection();
    }
    
